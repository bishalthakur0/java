import java.util.*;
public class patternB {
    public static void main(String[] args){
        int n=10;
        int m=12;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=m;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
    
}
